#!/usr/bin/env node
/**
 * export-presentation.js
 * Shows different export/output methods for a pptxgenjs presentation.
 * Usage: node export-presentation.js [--buffer] [--base64] [output-dir]
 */

const PptxGenJS = require('pptxgenjs');
const fs = require('fs');
const path = require('path');

const args = process.argv.slice(2);
const outputDir = args.find((a) => !a.startsWith('--')) || __dirname;
const doBuffer = args.includes('--buffer');
const doBase64 = args.includes('--base64');

function buildSamplePres() {
  const pres = new PptxGenJS();
  pres.title = 'Export Demo';
  const s = pres.addSlide();
  s.background = { fill: '1F4E79' };
  s.addText('Export Demo', { x: 1, y: 2, w: 8, h: 1.5, fontSize: 44, bold: true, color: 'FFFFFF', align: 'center' });
  s.addText('Generated by Dreamcobots', { x: 1, y: 3.8, w: 8, h: 0.5, fontSize: 18, color: '9DC3E6', align: 'center' });
  return pres;
}

async function main() {
  const pres = buildSamplePres();

  if (doBuffer) {
    // Export as Node.js Buffer
    const buffer = await pres.write({ outputType: 'nodebuffer' });
    const filePath = path.join(outputDir, 'export-buffer.pptx');
    fs.writeFileSync(filePath, buffer);
    console.log(`Buffer export: ${filePath} (${(buffer.length / 1024).toFixed(1)} KB)`);

  } else if (doBase64) {
    // Export as base64 string (useful for API responses / embedding)
    const b64 = await pres.write({ outputType: 'base64' });
    const filePath = path.join(outputDir, 'export-base64.txt');
    fs.writeFileSync(filePath, b64);
    console.log(`Base64 export: ${filePath} (${(b64.length / 1024).toFixed(1)} KB)`);
    console.log('Use as: data:application/vnd.openxmlformats-officedocument.presentationml.presentation;base64,' + b64.substring(0, 30) + '...');

  } else {
    // Default: writeFile
    const filePath = path.join(outputDir, 'export-demo.pptx');
    await pres.writeFile({ fileName: filePath });
    const stat = fs.statSync(filePath);
    console.log(`File export: ${filePath} (${(stat.size / 1024).toFixed(1)} KB)`);
  }

  console.log('\nExport options:');
  console.log('  node export-presentation.js              → writes .pptx file');
  console.log('  node export-presentation.js --buffer     → Buffer → .pptx file');
  console.log('  node export-presentation.js --base64     → base64 string → .txt');
}

main().catch((err) => { console.error(err); process.exit(1); });
