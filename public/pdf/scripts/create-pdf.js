#!/usr/bin/env node
/**
 * create-pdf.js
 * Creates a professional PDF document using PDFKit.
 * Usage: node create-pdf.js [output.pdf]
 */

const PDFDocument = require('pdfkit');
const fs = require('fs');
const path = require('path');

const outputPath = process.argv[2] || path.join(__dirname, 'output.pdf');

function createPdf() {
  const doc = new PDFDocument({
    size: 'A4',
    margins: { top: 60, bottom: 60, left: 72, right: 72 },
    bufferPages: true,
    info: {
      Title: 'Sample Report',
      Author: 'Dreamcobots',
      Subject: 'PDF Generation Demo',
      Keywords: 'pdf, pdfkit, report',
      CreationDate: new Date(),
    },
  });

  const stream = fs.createWriteStream(outputPath);
  doc.pipe(stream);

  const W = doc.page.width - 144;  // width minus margins
  const BLUE  = '#1F4E79';
  const LBLUE = '#DEEAF1';
  const GRAY  = '#555555';

  // ── Cover section ────────────────────────────────────────────────────────────
  doc.rect(0, 0, doc.page.width, 200).fill(BLUE);
  doc.fillColor('white').font('Helvetica-Bold').fontSize(28)
     .text('Sample Report', 72, 60, { width: W, align: 'center' });
  doc.fontSize(14).font('Helvetica')
     .text('Generated by Dreamcobots', 72, 105, { width: W, align: 'center' });
  doc.fontSize(11)
     .text(new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' }),
       72, 135, { width: W, align: 'center' });

  // ── Section helper ───────────────────────────────────────────────────────────
  function sectionHeading(title) {
    doc.moveDown(1);
    doc.rect(72, doc.y, W, 28).fill(BLUE);
    doc.fillColor('white').font('Helvetica-Bold').fontSize(13)
       .text(title, 80, doc.y - 22, { width: W - 16 });
    doc.fillColor('black').font('Helvetica').fontSize(11);
    doc.moveDown(0.5);
  }

  function bodyText(text) {
    doc.fillColor(GRAY).font('Helvetica').fontSize(11)
       .text(text, { align: 'justify', lineGap: 3 });
    doc.moveDown(0.5);
  }

  // ── Executive Summary ────────────────────────────────────────────────────────
  doc.moveDown(2);
  sectionHeading('Executive Summary');
  bodyText(
    'This report provides a comprehensive overview of business performance for the reporting period. ' +
    'Key metrics demonstrate strong growth across all major product lines, with particularly impressive ' +
    'results in the Asia Pacific and North American markets. Revenue exceeded targets by 18%, driven by ' +
    'increased customer acquisition and expanded product offerings.'
  );

  // ── Key Metrics ──────────────────────────────────────────────────────────────
  sectionHeading('Key Metrics');

  const metrics = [
    { label: 'Total Revenue', value: '$12.4M', change: '+18%' },
    { label: 'Gross Margin',  value: '38.2%',  change: '+2.1pp' },
    { label: 'Customers',     value: '1,847',  change: '+312' },
    { label: 'Net Income',    value: '$2.1M',  change: '+24%' },
  ];

  const metricBoxW = (W - 30) / 4;
  let mX = 72;
  const mY = doc.y + 5;

  metrics.forEach((m) => {
    doc.rect(mX, mY, metricBoxW, 65).fillAndStroke(LBLUE, '#9DC3E6');
    doc.fillColor(GRAY).font('Helvetica').fontSize(9)
       .text(m.label, mX + 6, mY + 8, { width: metricBoxW - 12 });
    doc.fillColor(BLUE).font('Helvetica-Bold').fontSize(18)
       .text(m.value, mX + 6, mY + 22, { width: metricBoxW - 12 });
    doc.fillColor('#276221').font('Helvetica-Bold').fontSize(11)
       .text(m.change, mX + 6, mY + 46, { width: metricBoxW - 12, align: 'right' });
    mX += metricBoxW + 10;
  });

  doc.moveDown(5.5);

  // ── Regional Analysis ────────────────────────────────────────────────────────
  sectionHeading('Regional Analysis');

  const regions = [
    { name: 'North America', rev: '$4.8M', share: '39%', growth: '+21%' },
    { name: 'Europe',        rev: '$3.2M', share: '26%', growth: '+14%' },
    { name: 'Asia Pacific',  rev: '$2.9M', share: '23%', growth: '+29%' },
    { name: 'Other',         rev: '$1.5M', share: '12%', growth: '+8%' },
  ];

  // Table header
  const cols = [180, 100, 80, 80];
  const headers = ['Region', 'Revenue', 'Share', 'Growth'];
  let tX = 72;
  const tY = doc.y + 5;

  headers.forEach((h, i) => {
    doc.rect(tX, tY, cols[i], 22).fill(BLUE);
    doc.fillColor('white').font('Helvetica-Bold').fontSize(10)
       .text(h, tX + 5, tY + 6, { width: cols[i] - 10, align: 'center' });
    tX += cols[i];
  });

  regions.forEach((r, ri) => {
    tX = 72;
    const rowY = tY + 22 + ri * 24;
    const rowFill = ri % 2 === 0 ? 'white' : '#F5F5F5';
    const vals = [r.name, r.rev, r.share, r.growth];
    vals.forEach((v, ci) => {
      doc.rect(tX, rowY, cols[ci], 24).fillAndStroke(rowFill, '#CCCCCC');
      doc.fillColor(GRAY).font('Helvetica').fontSize(10)
         .text(v, tX + 5, rowY + 7, { width: cols[ci] - 10, align: ci === 0 ? 'left' : 'center' });
      tX += cols[ci];
    });
  });

  doc.moveDown(5.5);

  // ── Conclusion ───────────────────────────────────────────────────────────────
  sectionHeading('Conclusion');
  bodyText(
    'The business continues to show strong growth momentum. Strategic investments in product development ' +
    'and market expansion are delivering measurable results. The outlook for the coming quarter remains ' +
    'positive, with pipeline growth and new partnerships expected to drive continued performance.'
  );

  // ── Page numbers ─────────────────────────────────────────────────────────────
  const totalPages = doc.bufferedPageRange().count;
  for (let i = 0; i < totalPages; i++) {
    doc.switchToPage(i);
    doc.fillColor('#999999').font('Helvetica').fontSize(8)
       .text(`Page ${i + 1} of ${totalPages}`,
         72, doc.page.height - 40, { width: W, align: 'center' });
  }

  doc.flushPages();
  doc.end();

  stream.on('finish', () => {
    const stat = fs.statSync(outputPath);
    console.log(`PDF created: ${outputPath} (${(stat.size / 1024).toFixed(1)} KB)`);
  });
}

createPdf();
