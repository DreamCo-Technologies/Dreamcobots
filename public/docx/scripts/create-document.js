#!/usr/bin/env node
/**
 * create-document.js
 * Creates a fully-formatted Word document using the docx npm package.
 * Usage: node create-document.js [output-path]
 */

const {
  Document,
  Packer,
  Paragraph,
  TextRun,
  HeadingLevel,
  AlignmentType,
  Header,
  Footer,
  PageNumber,
  BorderStyle,
  ShadingType,
  PageOrientation,
} = require('docx');
const fs = require('fs');
const path = require('path');

const outputPath = process.argv[2] || path.join(__dirname, 'output.docx');

async function createDocument() {
  const doc = new Document({
    title: 'Sample Document',
    creator: 'Dreamcobots',
    description: 'Auto-generated Word document',
    sections: [
      {
        headers: {
          default: new Header({
            children: [
              new Paragraph({
                alignment: AlignmentType.RIGHT,
                children: [
                  new TextRun({ text: 'DreamCo Technologies', bold: true, size: 18, color: '1F4E79' }),
                ],
              }),
            ],
          }),
        },
        footers: {
          default: new Footer({
            children: [
              new Paragraph({
                alignment: AlignmentType.CENTER,
                children: [
                  new TextRun('Page '),
                  new TextRun({ children: [PageNumber.CURRENT] }),
                  new TextRun(' of '),
                  new TextRun({ children: [PageNumber.TOTAL_PAGES] }),
                ],
              }),
            ],
          }),
        },
        children: [
          // Title
          new Paragraph({
            text: 'Sample Document Title',
            heading: HeadingLevel.TITLE,
            alignment: AlignmentType.CENTER,
            spacing: { after: 400 },
          }),

          // Subtitle
          new Paragraph({
            alignment: AlignmentType.CENTER,
            spacing: { after: 600 },
            children: [
              new TextRun({ text: 'Generated by Dreamcobots', italics: true, size: 24, color: '555555' }),
            ],
          }),

          // Section heading
          new Paragraph({
            text: 'Introduction',
            heading: HeadingLevel.HEADING_1,
            spacing: { before: 400, after: 200 },
          }),

          // Body text
          new Paragraph({
            spacing: { after: 200, line: 360 },
            children: [
              new TextRun(
                'This document was created programmatically using the '
              ),
              new TextRun({ text: 'docx', bold: true, font: 'Courier New' }),
              new TextRun(
                ' npm package. It demonstrates common formatting options including headings, ' +
                'paragraphs, text styling, and structural elements.'
              ),
            ],
          }),

          // Subheading
          new Paragraph({
            text: 'Key Features',
            heading: HeadingLevel.HEADING_2,
            spacing: { before: 300, after: 150 },
          }),

          // Feature paragraphs
          ...[
            'Rich text formatting with bold, italic, underline, and color',
            'Tables with custom borders, shading, and merged cells',
            'Images with inline and floating placement',
            'Headers and footers with page numbers',
            'Custom paragraph and character styles',
          ].map(
            (feature) =>
              new Paragraph({
                spacing: { after: 100 },
                children: [
                  new TextRun({ text: '• ', bold: true, color: '1F4E79' }),
                  new TextRun(feature),
                ],
              })
          ),

          // Highlighted callout
          new Paragraph({
            spacing: { before: 300, after: 300 },
            shading: { type: ShadingType.SOLID, color: 'E7F3FF' },
            border: {
              left: { style: BorderStyle.THICK, size: 12, color: '1F4E79', space: 10 },
            },
            children: [
              new TextRun({
                text: 'Note: This document can be customised by modifying the source script and re-running it.',
                italics: true,
                color: '1F4E79',
              }),
            ],
          }),

          // Conclusion
          new Paragraph({
            text: 'Conclusion',
            heading: HeadingLevel.HEADING_1,
            spacing: { before: 400, after: 200 },
          }),
          new Paragraph({
            text: 'This template can be extended with tables, images, lists, and multi-section layouts to produce professional reports and documents.',
            spacing: { after: 200, line: 360 },
          }),
        ],
      },
    ],
  });

  const buffer = await Packer.toBuffer(doc);
  fs.writeFileSync(outputPath, buffer);
  console.log(`Document created: ${outputPath} (${(buffer.length / 1024).toFixed(1)} KB)`);
}

createDocument().catch((err) => {
  console.error('Error creating document:', err.message);
  process.exit(1);
});
